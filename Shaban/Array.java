public class Array {
    public static void main(String[] args) {


        int[] drinks = {1,2,3,4,5};
        System.out.println(drinks[3]);
        

        int[] myArray = {1,2,3,4,5};
        System.out.println(myArray[0]);

        String[] people = {"Caroline", "Brian", "Njenga"};
        people[1] = "Shuaib Shaban";
        System.out.println("The newly added name at index 1 is: " + people[1]);
    }
}
